#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	unsigned int i;
	unsigned int j;

	if (needle[0] == '\0')
		return ((char *)haystack);
	i = 0;
	while (haystack[i] != '\0')
	{
		j = i;
		while (needle[j - i] == haystack[j])
			if (needle[++j - i] == '\0')
				return ((char *)haystack + i);
		i++;
	}
	return (NULL);
}
