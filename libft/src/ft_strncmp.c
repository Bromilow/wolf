#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while ((*s1 == *s2) && (*s1 != '\0') && (*s2 != '\0') && n--)
	{
		s1++;
		s2++;
	}
	return (*(char *)s1 - *(char *)s2);
}
